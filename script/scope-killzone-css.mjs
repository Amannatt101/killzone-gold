/**
 * Merge new-desing/styles.css + styles-v2.css and scope selectors under .kz-v2
 * so Tailwind/shadcn globals are not overwritten.
 */
import fs from "fs";
import path from "path";
import postcss from "postcss";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function stripImportAndGlobals(src) {
  let s = src.replace(/@import\s+url\([^)]+\)\s*;/g, "");
  s = s.replace(/\*\s*\{[^}]*\}\s*/s, "");
  s = s.replace(/html\s*,\s*body\s*,\s*#root\s*\{[^}]*\}\s*/s, "");
  s = s.replace(/:root\s*\{/, ".kz-v2 {");
  s = s.replace(/body\.is-dragging/g, ".kz-v2.kz-v2--dragging");
  return s;
}

const scopedPlugin = () => ({
  postcssPlugin: "scope-killzone-v2",
  Rule(rule, { Rule: RuleNode }) {
    if (!(rule instanceof RuleNode)) return;
    let p = rule.parent;
    while (p) {
      if (p.type === "atrule" && /keyframes/i.test(p.name)) return;
      p = p.parent;
    }
    const sel = (rule.selector || "").trim();
    if (sel === ".kz-v2") return;
    if (sel.startsWith(".kz-v2 ") || sel === ".kz-v2") return;
    rule.selectors = rule.selectors.map((s) => {
      const t = s.trim();
      if (t.startsWith("@")) return t;
      return `.kz-v2 ${t}`;
    });
  },
});
scopedPlugin.postcss = true;

const stylesPath = path.join(root, "new-desing/styles.css");
const stylesV2Path = path.join(root, "new-desing/styles-v2.css");
let combined =
  stripImportAndGlobals(fs.readFileSync(stylesPath, "utf8")) +
  "\n" +
  fs.readFileSync(stylesV2Path, "utf8");

const shell = `
/* Shell typography + layout (scoped): replaces prototype html/body/#root rules */
.kz-v2.kz-v2-shell {
  width: 100%;
  height: 100vh;
  min-height: 100vh;
  overflow: hidden;
  background: var(--bg-0);
  color: var(--text-1);
  font-family: 'Geist', 'Inter', -apple-system, system-ui, sans-serif;
  font-size: 13px;
  line-height: 1.5;
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
  font-feature-settings: 'ss01', 'cv11';
}
.kz-v2.kz-v2-shell .mono {
  font-family: 'Geist Mono', 'JetBrains Mono', ui-monospace, monospace;
  font-feature-settings: 'zero', 'ss01';
}
.kz-v2.kz-v2-shell ::selection {
  background: var(--gold-glow);
  color: var(--text-0);
}
`;

const result = await postcss([scopedPlugin]).process(combined, { from: undefined });

const outDir = path.join(root, "client/src/styles");
const outPath = path.join(outDir, "killzone-v2.css");
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outPath, `/* Auto-generated by script/scope-killzone-css.mjs — do not edit by hand */\n${shell}\n${result.css}\n`);
console.log("Wrote", outPath, `(${result.css.length + shell.length} chars)`);
